number=int(input("введите целое число:")) #запрос на целое число от пользователя
if (number % 2 == 0): #прописываем оператор if/else
    print("число является четным.") #вывод текста, если число кратно 2
else: #если условие ложное
    print("число является нечетным.") #вывод текста, если число не кратно 2